
public class Default 
{
	byte a;
	short b;
	int c;
	long d;
	float e;
	double f;
	char g;
	boolean h;
		public static void main(String[] args)
	{
		Default ob= new Default();
		System.out.println("Demonstration of Primitive Datatype Default Value Without using Static ");
		System.out.println("Default Value of Byte is "+ob.a);
		System.out.println("Default Value of Short is "+ob.b);
		System.out.println("Default Value of Int is "+ob.c);
		System.out.println("Default Value of Long is "+ob.d);
		System.out.println("Default Value of Float is "+ob.e);
		System.out.println("Default Value of Double is "+ob.f);
		System.out.println("Default Value of Char is "+ob.g);
		System.out.println("Default Value of Boolean is "+ob.h);
	}

}
